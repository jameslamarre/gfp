/** @type {import('tailwindcss').Config} */

function rem(num, base = 16) {
	let parsedNum = num;

	// remove mesurment unit
	if (typeof num == "string") {
		parsedNum = Number(num.replace(/(\d.)(.*)/, "$1"));
	}

	parsedNum = `${parsedNum / base}rem`;

	return parsedNum;
}

